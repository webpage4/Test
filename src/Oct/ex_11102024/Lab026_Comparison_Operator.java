package Oct.ex_11102024;

public class Lab026_Comparison_Operator {
    public static void main(String[] args) {
        //Comparison operator
        System.out.println(10 == 10);
        System.out.println( 10 == 10);
        System.out.println( 10 == 20);
        System.out.println( 10 < 10);
        System.out.println( 10 <= 10); // 10 < 10 or 10 = 10
        System.out.println( 10 >= 10); //  10> 10 or 10 = 10




    }
}
