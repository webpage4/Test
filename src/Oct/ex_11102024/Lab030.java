package Oct.ex_11102024;

public class Lab030 {
    public static void main(String[] args) {
        // Compound Assignment Operators
        // +=, -=, /=, *=
        int a= 10;
        a +=10; // a = a+10
        System.out.println(a);
        a -=10; // a = a-10
        System.out.println(a);
        a /=10; // a = a/10
        System.out.println(a);
        a *=10; // a = a*10
        System.out.println(a);

    }
}
